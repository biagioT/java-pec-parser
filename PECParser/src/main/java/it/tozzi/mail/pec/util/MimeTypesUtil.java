	public static String getMimeType(String extension) {
		return mimeTypesMap.entrySet().stream().filter(e -> e.getValue().contains(extension.toLowerCase())).findFirst().map(e -> e.getKey()).orElse(CONTENT_TYPE_OCETSTREAM);
	}

	public static String getExtension(String fullMimeType) {
		return mimeTypesMap.entrySet().stream().filter(e -> fullMimeType.toLowerCase().contains(e.getKey().toLowerCase())).findFirst().map(e -> e.getValue().get(0)).orElse(null);
	}
